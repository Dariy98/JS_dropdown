const buttons = document.querySelectorAll(".dropdown");
const menu = document.querySelectorAll(".dropdown-menu");

document.querySelector("body").addEventListener("click", event => {
  buttons.forEach(button => {
    button.classList.remove("show");
  });
  menu.forEach(item => {
    item.classList.remove("show");
  });
  if (event.target.dataset.toggle === "dropdown") {
    let i = JSON.parse(event.target.dataset.item);
    if (event.target.classList.contains("show")) {
      event.target.classList.remove("show");
      menu[i].classList.remove("show");
    } else {
      event.target.classList.add("show");
      menu[i].classList.add("show");
    }
  }
});
